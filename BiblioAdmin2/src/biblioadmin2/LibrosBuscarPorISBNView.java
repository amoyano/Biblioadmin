    @Action
    public void showLibroModificar() {
   
      ModelLocator.getInstance().libroSeleccionado = (Libro) this.listLibros.getSelectedValue();
      if (libroModificarView == null) {
            JFrame mainFrame = BiblioAdmin2App.getApplication().getMainFrame();
            libroModificarView = new LibrosModificarView();
            libroModificarView.setLocationRelativeTo(mainFrame);
        }
        libroModificarView.show();
    


    }

    @Action
    public void buscarLibroISBN() {
        ObjectSet<Libro> libros;


        libros  = LibroManager.getInstance().buscar(this.txtISBN.getText(), null, null);

//PREGUNTAR TULIO PARA QUE SE VEA EL CAMPO TITULO Y QUE ACEPTE OBJECTSET LA LISTA
      this.listLibros.setListData(libros.toArray());
    }
    
